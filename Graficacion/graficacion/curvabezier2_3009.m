#curvas de bezier
clear all
t=0:0.01:1;
#P=[1,5;5,14;-3,2];
#P=[1,5;5,14;-3,2;12,-15];
P=[10,15;2,2;-5,14;-7,5];
Q=[12,15;-3,2;5,14;10,5];

Bx2(:,1)=(1-t).^3*P(1,1)+3*(1-t).^2.*t*P(2,1)+3*(1-t).*t.^2*P(3,1)+t.^3*P(4,1);
Bx2(:,2)=(1-t).^3*P(1,2)+3*(1-t).^2.*t*P(2,2)+3*(1-t).*t.^2*P(3,2)+t.^3*P(4,2);
Qx2(:,1)=(1-t).^3*Q(1,1)+3*(1-t).^2.*t*Q(2,1)+3*(1-t).*t.^2*Q(3,1)+t.^3*Q(4,1);
Qx2(:,2)=(1-t).^3*Q(1,2)+3*(1-t).^2.*t*Q(2,2)+3*(1-t).*t.^2*Q(3,2)+t.^3*Q(4,2);
#line(P(:,1),P(:,2),'linewidth',3,'color','k')

for i =1:length(t)
s=1/i;
C(i,1)=(1-s)*Bx2(i,1)+s*Qx2(i,1);
C(i,2)=(1-s)*Bx2(i,2)+s*Qx2(i,2);
plot(C(:,1),C(:,2),'linewidth',3,'color','b')
pause(0.5)
clf
end